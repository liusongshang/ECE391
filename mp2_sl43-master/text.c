/*  void convert_to_image
 *   Description: Given a string, it should produce a buffer that holds a graphical image of the ASCII characters in the string.
 *           
 *   INPUTS: message--status_msg (dispalyed at center)
 *           cmd--typed command  (displayed on the right)
 *           room--room name (displayed on the left)
 *           buf--hold the bar's information, size is 4*1440       
 *   OUTPUTS: none
 *   RETURN VALUE: none 
 *   SIDE EFFECTS: change the buffer tp contain text informatino.
 */
void 
convert_to_image(const char* message, const char* cmd, const char* room, unsigned char* buf){
    int size;   //the size of the message
    int position;   //the stating position of the string
    char realcmd[2*MAX_CMD_LENGTH];   //contain the typd command + suffix'_'
    strcpy(realcmd,cmd);
    if(message[0] != '\0') {  
        position = IMAGE_X_WIDTH/2 - strlen(message);   // change the position to center
        size = strlen(message);
        char_write_to_memory(position,message,buf,size); 
    }
    else{
        //Write the room name
        position = 0;   //change the position to the left
        size = strlen(room);
        char_write_to_memory(position,room,buf,size);
        //Write the typed command
        if(strlen(cmd)<MAX_CMD_LENGTH){ // if cmd's size <20, add suffix
            realcmd[strlen(cmd)] = '_'; 
            position =IMAGE_X_WIDTH - (strlen(cmd)+1)*2;   
            size = strlen(cmd);
            char_write_to_memory(position,realcmd,buf,size+1);
        }   
        if(strlen(cmd)==MAX_CMD_LENGTH){    // if cmd'size is 20, don't add suffix
            position = IMAGE_X_WIDTH - strlen(cmd)*2;
            size = strlen(cmd);
            char_write_to_memory(position,realcmd,buf,size);    // if not adding the suffix, don't let size+1
        }
        
    }
}
/*  void convert_to_image
 *   Description: a helper function that writes to buffer pixel by pixel
 *   INPUTS: message--status_msg (dispalyed at center)
 *           cmd--typed command  (displayed on the right)
 *           room--room name (displayed on the left)
 *           buf--hold the bar's information, size is 4*1440       
 *   OUTPUTS: none
 *   RETURN VALUE: none 
 *   SIDE EFFECTS: change the buffer to contain text information.
 */
void
char_write_to_memory(int position,const char*str,unsigned char*buf,int size)
{  
    int char_index; // index for the characters
    int check_bit;  //8-bit bitmask
    int char_ascii; // ascii of the character
    int cur_column; //current column of the character
    int i,j;    //loop counter
    for(char_index=0;char_index<size;char_index++){
        char_ascii = (int)str[char_index];
        cur_column = 2*char_index+position;
        for(i = 0; i < FONT_HEIGHT;i++){
            check_bit = BIT_MASK;    //1000 0000
            for(j = 0; j < FONT_WIDTH; j++){
                if((font_data[char_ascii][i] & check_bit)  ==check_bit ){
                    int p_off = j&3; // get the offset 
                    int addr = p_off*STATUS_BUFFER_SIZE + IMAGE_X_WIDTH*(i+1) + cur_column+j/VGA_ADDR_SIZE; //find the plane, plus the position
                    buf[addr] = YELLOW_COLLOR;
                }
                check_bit = check_bit/2;    //right shift 1 bit
            }
        }
    }
} 
